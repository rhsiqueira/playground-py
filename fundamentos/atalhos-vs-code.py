# ============================================================
# 🎹 Treino de Atalhos VS Code
# ============================================================
# ✅ Objetivo:
# Praticar diariamente os atalhos essenciais do VS Code
# para ganhar velocidade e fluidez na edição de código.
#
# ✅ Atalhos para treinar:
#
# 1. Seleção e edição
# - Ctrl + D → selecionar próxima ocorrência
# - Alt + Click → múltiplos cursores
# - Alt + ↑ / ↓ → mover linha
# - Shift + Alt + ↓ → duplicar linha
# - Ctrl + Shift + K → deletar linha inteira
#
# 2. Comentários e formatação
# - Ctrl + / → comentar/descomentar linha
# - Shift + Alt + A → comentar bloco
# - Shift + Alt + F → formatar o código
#
# 3. Busca e navegação
# - Ctrl + F → buscar no arquivo
# - Ctrl + Shift + F → buscar no projeto
# - Ctrl + P → abrir arquivos rapidamente
# - Ctrl + Tab → alternar entre abas abertas
#
# 4. Terminal e comandos
# - Ctrl + ' → abrir/fechar terminal integrado
# - Ctrl + Shift + P → paleta de comandos
#
# 🚀 Mini treino diário (sugestão):
# 1. Duplicar essa linha 3 vezes → (Shift + Alt + ↓)
print("Linha duplicada")
print("Linha duplicada")
print("Linha duplicada")
#
# 2. Selecionar a palavra TESTE em todas as ocorrências → (Ctrl + D)
print("TESTE")
print("TESTE")
#
# 3. Comentar e descomentar essa linha → (Ctrl + /)
print("Comente-me")
#
# 4. Desorganizar o código abaixo e depois arrumar com (Shift + Alt + F)
if True:
      print(  "Código mal indentado"  )
#
# 5. Abrir terminal e rodar este arquivo → (Ctrl + ' e python atalhos-vs-code.py)
#
